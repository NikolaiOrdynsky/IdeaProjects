package AleksandrVasko.oop.objectWork.objectWork2;

/**
 * The type Car.
 */
public class car {
    /**
     * The Color.
     */
    String color;
    /**
     * The Engine.
     */
    String engine;
    /**
     * The Door.
     */
    int door;

    /**
     * Instantiates a new Car.
     *
     * @param color  the color
     * @param engine the engine
     * @param door   the door
     */
    car(String color, String engine, int door) {
        this.color = color;
        this.door = door;
        this.engine = engine;
    }
}

