/**
 * The type Auto.
 */
public abstract class auto {


    /**
     * Plus speed.
     *
     * @param currentSpeed the current speed
     */
    public abstract void plusSpeed(int currentSpeed);

    /**
     * Minus speed.
     *
     * @param currentSpeed the current speed
     */
    public abstract void minusSpeed(int currentSpeed);
}
